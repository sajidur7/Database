-- Create Users table
-- DROP DATABASE medicine_buying_app;
CREATE DATABASE medicine_buying_app;
USE medicine_buying_app;
-- Users table
CREATE TABLE Users (
    UserID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(255),
    Email VARCHAR(255),
    Phone VARCHAR(20),
    Address VARCHAR(255)
);

-- LoginCredentials table
CREATE TABLE LoginCredentials (
    LoginID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    Username VARCHAR(50),
    Password VARCHAR(255),
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

-- Categories table
CREATE TABLE Categories (
    CategoryID INT AUTO_INCREMENT PRIMARY KEY,
    CategoryName VARCHAR(100)
);

-- Medicines table
CREATE TABLE Medicines (
    MedicineID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(255),
    Description TEXT,
    CategoryID INT,
    FOREIGN KEY (CategoryID) REFERENCES Categories(CategoryID)
);

-- Cart table
CREATE TABLE Cart (
    CartID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    MedicineID INT,
    Quantity INT,
    FOREIGN KEY (UserID) REFERENCES Users(UserID),
    FOREIGN KEY (MedicineID) REFERENCES Medicines(MedicineID)
);

-- Orders table
CREATE TABLE Orders (
    OrderID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    OrderDate DATE,
    TotalAmount DECIMAL(10, 2),
    PaymentMethod VARCHAR(50),
    PaymentDetails VARCHAR(255),
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

-- PrescriptionUploads table
CREATE TABLE PrescriptionUploads (
    UploadID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    PrescriptionImage VARCHAR(255),
    VerificationStatus BOOLEAN,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

-- OrderStatus table
CREATE TABLE OrderStatus (
    StatusID INT AUTO_INCREMENT PRIMARY KEY,
    StatusName VARCHAR(50)
);

-- OrderTracking table
CREATE TABLE OrderTracking (
    TrackingID INT AUTO_INCREMENT PRIMARY KEY,
    OrderID INT,
    StatusID INT,
    UpdateDateTime DATETIME,
    FOREIGN KEY (OrderID) REFERENCES Orders(OrderID),
    FOREIGN KEY (StatusID) REFERENCES OrderStatus(StatusID)
);

-- Refills table
CREATE TABLE Refills (
    RefillID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    MedicineID INT,
    RefillDate DATE,
    FOREIGN KEY (UserID) REFERENCES Users(UserID),
    FOREIGN KEY (MedicineID) REFERENCES Medicines(MedicineID)
);

-- ReminderAlerts table
CREATE TABLE ReminderAlerts (
    AlertID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    AlertType VARCHAR(100),
    AlertDateTime DATETIME,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

-- CustomerSupportChat table
CREATE TABLE CustomerSupportChat (
    ChatID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    ChatDateTime DATETIME,
    Message TEXT,
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

-- Feedback table
CREATE TABLE Feedback (
    FeedbackID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    OrderID INT,
    Rating INT,
    Comment TEXT,
    FOREIGN KEY (UserID) REFERENCES Users(UserID),
    FOREIGN KEY (OrderID) REFERENCES Orders(OrderID)
);

-- Promotions table
CREATE TABLE Promotions (
    PromotionID INT AUTO_INCREMENT PRIMARY KEY,
    PromotionName VARCHAR(255),
    Description TEXT,
    DiscountPercentage DECIMAL(5, 2)
);

-- Junction table for many-to-many relationship between Medicines and Promotions
CREATE TABLE MedicinePromotions (
    MedicineID INT,
    PromotionID INT,
    PRIMARY KEY (MedicineID, PromotionID),
    FOREIGN KEY (MedicineID) REFERENCES Medicines(MedicineID),
    FOREIGN KEY (PromotionID) REFERENCES Promotions(PromotionID)
);

-- UserAccountSettings table
CREATE TABLE UserAccountSettings (
    SettingID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    SettingName VARCHAR(100),
    SettingValue VARCHAR(255),
    FOREIGN KEY (UserID) REFERENCES Users(UserID)
);

-- OrderHistory table
CREATE TABLE OrderHistory (
    HistoryID INT AUTO_INCREMENT PRIMARY KEY,
    UserID INT,
    OrderID INT,
    OrderDate DATE,
    TotalAmount DECIMAL(10, 2),
    FOREIGN KEY (UserID) REFERENCES Users(UserID),
    FOREIGN KEY (OrderID) REFERENCES Orders(OrderID)
);


